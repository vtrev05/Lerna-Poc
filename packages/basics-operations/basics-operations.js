function add(a, b) {
    return a + b;
  }
  
  function substract(a, b) {
    return a - b;
  }
  
  function multiply(a, b) {
    return a * b;
  }
  
  function divide(a, b) {
    return a / b;
  }
  
  exports.add = add;
  exports.substract = substract;
  exports.multiply = multiply;
  exports.divide = divide;